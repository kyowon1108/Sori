export default function EventTimeline({
  events,
  loading = false,
  maxItems = 20,
  showViewAll = true,
}: EventTimelineProps) {
  const displayEvents = events.slice(0, maxItems);

  if (loading) {
    return (
      <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
        <h2 className="text-lg font-semibold text-gray-900 mb-4">최근 이벤트</h2>
        <div className="space-y-4">
          {[1, 2, 3, 4, 5].map((i) => (
            <div key={i} className="animate-pulse flex items-start gap-3">
              <div className="w-8 h-8 bg-gray-200 rounded-full" />
              <div className="flex-1 space-y-2">
                <div className="h-4 bg-gray-200 rounded w-3/4" />
                <div className="h-3 bg-gray-200 rounded w-1/2" />
              </div>
              <div className="h-3 bg-gray-200 rounded w-16" />
            </div>
          ))}
        </div>
      </div>
    );
  }

  return (
    <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
      <div className="flex items-center justify-between mb-4">
        <h2 className="text-lg font-semibold text-gray-900">최근 이벤트</h2>
        {showViewAll && events.length > 0 && (
          <Link
            href="/alerts"
            className="text-sm text-blue-600 hover:text-blue-700"
          >
            전체 보기
          </Link>
        )}
      </div>

      {displayEvents.length === 0 ? (
        <EmptyState
          title="이벤트가 없습니다"
          description="아직 기록된 이벤트가 없습니다."
          icon={
            <svg className="w-12 h-12 text-gray-300" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M19 11H5m14 0a2 2 0 012 2v6a2 2 0 01-2 2H5a2 2 0 01-2-2v-6a2 2 0 012-2m14 0V9a2 2 0 00-2-2M5 11V9a2 2 0 012-2m0 0V5a2 2 0 012-2h6a2 2 0 012 2v2M7 7h10" />
            </svg>
          }
        />
      ) : (
        <div className="relative">
          {/* Timeline line */}
          <div className="absolute left-4 top-0 bottom-0 w-0.5 bg-gray-200" />

          <div className="space-y-4">
            {displayEvents.map((event) => {
              const colors = EVENT_SEVERITY_COLORS[event.severity];
              const icon = EVENT_TYPE_ICONS[event.type];

              return (
                <div key={event.id} className="relative flex items-start gap-3 pl-1">
                  {/* Icon */}
                  <div
                    className={clsx(
                      'relative z-10 flex-shrink-0 w-8 h-8 rounded-full flex items-center justify-center ring-4 ring-white',
                      colors.bg,
                      colors.icon
                    )}
                  >
                    {icon}
                  </div>

                  {/* Content */}
                  <div className="flex-1 min-w-0 pb-4">
                    {event.cta ? (
                      <Link
                        href={event.cta.href}
                        className="block hover:bg-gray-50 -mx-2 px-2 py-1 rounded transition-colors"
                      >
                        <div className="flex items-start justify-between gap-2">
                          <div className="min-w-0">
                            <p className="text-sm font-medium text-gray-900 truncate">
                              {event.title}
                            </p>
                            <p className="text-sm text-gray-500 truncate">
                              {event.description}
                            </p>
                          </div>
                          <span className="text-xs text-gray-400 whitespace-nowrap flex-shrink-0">
                            {formatRelativeTime(event.timestamp)}
                          </span>
                        </div>
                      </Link>
                    ) : (
                      <div className="flex items-start justify-between gap-2">
                        <div className="min-w-0">
                          <p className="text-sm font-medium text-gray-900 truncate">
                            {event.title}
                          </p>
                          <p className="text-sm text-gray-500 truncate">
                            {event.description}
                          </p>
                        </div>
                        <span className="text-xs text-gray-400 whitespace-nowrap flex-shrink-0">
                          {formatRelativeTime(event.timestamp)}
                        </span>
                      </div>
                    )}
                  </div>
                </div>
              );
            })}
          </div>
        </div>
      )}
    </div>
  );
}
